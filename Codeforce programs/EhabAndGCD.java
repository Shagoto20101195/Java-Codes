import java.util.*;
public class EhabAndGCD
{
  public static void main(String[]args)
  {
    Scanner read = new Scanner(System.in); 
    
    int t = read.nextInt();
    for(int x = 1; x<=t; x++)
    {
      int n = read.nextInt();
      
      System.out.println(1+" "+(n-1));
    }
  }
}